package Class.Comment_variable;

public class _03_Variable3 {
    public static void main(String[] args) {

        // 리터럴 상수의 구조

        // 접두사 + 상수 값 + 접미사
        System.out.println(10); // 정수 - int
        System.out.println(10L); // 정수 - long
        System.out.println(10.0F); // 실수 - float
        System.out.println(10.0); // 실수 - double
        System.out.println('1'); // 문자 char
        System.out.println(true); // boolean
        System.out.println(false); // boolean

        float pos = 2.0f + 3.0f;

        float kin = (float)(2.0 + 3.0);

    }
}
