package Back_Joon._09_21_;

import java.util.Scanner;

public class _10869_ {
    public static void main(String[] args) {
        Scanner scan = new Scanner (System.in);

        int A = scan.nextInt();
        int B = scan.nextInt();

        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A % B);
    }
}
